package coder25.problemSolving1.mphasis.jan8.utility;

public class ListNodes {
    public int val;
    public ListNode next;

    public ListNodes() {
    }

    public ListNodes(int val) {
        this.val = val;
    }

    public ListNodes(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}

